import { readFileSync, writeFileSync } from "node:fs";

const source =
  "Вопросы для самопроверки Информационная безопасность 10-й семестр.mdx";
const target = "src/selfCheckData.js";

const text = readFileSync(source, "utf-8");
const lines = text.split(/\r?\n/);
const groups = [];
let currentLevel = "";
let currentGroup = null;

for (const line of lines) {
  const h2 = line.match(/^##\s+(.+)$/);
  const h3 = line.match(/^###\s+(.+)$/);
  const question = line.match(/^\d+\.\s+(.+)$/);

  if (h2) {
    const title = stripMarkdown(h2[1]);
    if (
      [
        "Базовые вопросы",
        "Обычные вопросы",
        "Супер-вопросы",
        "Мини-кейсы для устной тренировки",
      ].includes(title)
    ) {
      currentLevel = title;
    }
    continue;
  }

  if (h3 && currentLevel) {
    currentGroup = {
      id: slug(`${currentLevel}-${stripMarkdown(h3[1])}`),
      level: currentLevel,
      title: stripMarkdown(h3[1]),
      questions: [],
    };
    groups.push(currentGroup);
    continue;
  }

  if (question && currentGroup) {
    const q = stripMarkdown(question[1]);
    const index = currentGroup.questions.length + 1;
    currentGroup.questions.push({
      id: `${currentGroup.id}-${index}`,
      question: q,
      answer: answerFor(q),
    });
  }
}

writeFileSync(
  target,
  `export const selfCheckGroups = ${JSON.stringify(groups, null, 2)};\n`,
  "utf-8",
);

console.log(
  `Generated ${groups.reduce((sum, group) => sum + group.questions.length, 0)} questions in ${target}`,
);

function stripMarkdown(value) {
  return value
    .replace(/<a id="[^"]+"><\/a>/g, "")
    .replace(/`([^`]+)`/g, "$1")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .trim();
}

function slug(value) {
  return value
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^a-zа-я0-9]+/gi, "-")
    .replace(/^-+|-+$/g, "");
}

